LINES={}
function BOOT()
	local code=loadCode()
	if code=="" then
		LINES=getBank(8)
	else
		-- trace(code)
		for s in code:gmatch("[^\r\n]+") do
			LINES[#LINES+1]=s
		end
	end
end

CURS_X=0
CURS_Y=0
FN=nil
MOD_CTRL=1
MOD_SHIFT=2
MOD_ALT=4
LAST_ASCII=nil
LAST_SPECIAL=nil

LOAD_CODE=false
LAST_ERR=nil
KEY_SP_CR="enter"
KEY_SP_U="up"
KEY_SP_D="down"
KEY_SP_L="left"
KEY_SP_R="right"
KEY_SP_TAB="tab"
KEY_SP_BS="backspace"
KEY_SP_DEL="delete"
KEY_SP_HOME="home"
KEY_SP_END="end"

function saveCode(code)
	pmem(0,#code)
	for i=1,#code do
	 local v=string.byte(code,i)

		pmem(i,v)
	end
end

function loadCode()
	local len=pmem(0)
 local v=""
	for i=1,len do
		v=v..string.char(pmem(i))
	end

	return v
end

T=0
function TIC()
	cls()

	if LOAD_CODE then
		fnOld=FN
		local code=table.concat(LINES,"\n")
	 saveCode(code)
		loadCode()
		
		FN,LAST_ERR=load(code)
		LOAD_CODE=false

		-- If we have an error then reinstate
		-- the last running code
		if LAST_ERR then
			FN=fnOld
		end		
	end

	if FN then
		local result,err=pcall(FN)
		if not result then
			LAST_ERR=err
		end
	end		

	local ascii,special,mods=getKey()
	if ascii==LAST_ASCII then
	 ascii=nil
	else
		LAST_ASCII=ascii
	end
	
	if special==LAST_SPECIAL then
	 special=nil
	else
		LAST_SPECIAL=special
	end

	if ascii then
		if (mods&MOD_CTRL)>0 then
			if ascii=="1" then
				LINES=getBank(1)
				CURS_X,CURS_Y=0,0
			elseif ascii=="2" then
				LINES=getBank(2)
				CURS_X,CURS_Y=0,0
			elseif ascii=="3" then
				LINES=getBank(3)
				CURS_X,CURS_Y=0,0
			elseif ascii=="8" then
				LINES=getBank(8)
				CURS_X,CURS_Y=0,0
			elseif ascii=="9" then
				LINES=getBank(9)
				CURS_X,CURS_Y=0,0
			elseif ascii=="0" then
				LOAD_CODE=true
			end
		else
			local l=LINES[CURS_Y+1]
			local cut=CURS_X
			LINES[CURS_Y+1]=
				l:sub(1,cut)..ascii..l:sub(cut+1)
			CURS_X=MAX(0,CURS_X+1)
		end
	elseif special==KEY_SP_CR then
		local l=LINES[CURS_Y+1]
		local cut=CURS_X
		CURS_X=0
		for y=#LINES+1,CURS_Y+3,-1 do
			LINES[y]=LINES[y-1]
		end
		LINES[CURS_Y+1]=l:sub(1,cut)
		LINES[CURS_Y+2]=l:sub(cut+1)

		CURS_Y=CURS_Y+1
	elseif special==KEY_SP_L then
		CURS_X=MAX(0,CURS_X-1)
	elseif special==KEY_SP_R then
		CURS_X=MIN(CURS_X+1,LINES[CURS_Y+1]:len())
	elseif special==KEY_SP_U then
		CURS_Y=MAX(0,CURS_Y-1)
	elseif special==KEY_SP_D then
		CURS_Y=MIN(CURS_Y+1,#LINES-1)
	elseif special==KEY_SP_HOME then
		CURS_X=0
	elseif special==KEY_SP_END then
		CURS_X=LINES[CURS_Y+1]:len()
	elseif special==KEY_SP_BS then
		if CURS_X>0 then
			local l=LINES[CURS_Y+1]
			local cut=CURS_X-1
			LINES[CURS_Y+1]=
				l:sub(1,cut)..l:sub(cut+2)
			CURS_X=MAX(0,CURS_X-1)
		else
			if CURS_Y>0 then
				CURS_X=LINES[CURS_Y]:len()
				LINES[CURS_Y]=LINES[CURS_Y]..LINES[CURS_Y+1]
				for y=CURS_Y+1,#LINES-1 do
					LINES[y]=LINES[y+1]
				end
				LINES[#LINES]=nil
				CURS_Y=CURS_Y-1
			end
		end
	elseif special==KEY_SP_DEL then
		local l=LINES[CURS_Y+1]
		if CURS_X<l:len() then
			local cut=CURS_X
			LINES[CURS_Y+1]=
				l:sub(1,cut)..l:sub(cut+2)
		elseif CURS_Y<#LINES-1 then
		 LINES[CURS_Y+1]=
				l..LINES[CURS_Y+2]
			for y=CURS_Y+2,#LINES do
				LINES[y]=LINES[y+1]
			end
		end
	end

	vbank(1)
	cls()
	-- draw cursor
	if T//32%2==0 then
		rect(CURS_X*6,CURS_Y*8,6,6,(T//4)%16)
	end

	for l=1,#LINES do
		print(LINES[l],1,(l-1)*8+1,1,true)
		print(LINES[l],0,(l-1)*8,12,true)
	end

	if LAST_ERR then
		print(LAST_ERR,1,130,15,false,1,true)
		print(LAST_ERR,0,129,2,false,1,true)
	end

	vbank(0)
	T=T+1
end

-- returns:
-- ascii of first (or nil)
-- special keycode (non-printable)
-- modifiers bitmap (or 0)
function getKey()
	local ascii=nil
	local special=nil
	local mods=0
	local letter=nil
	local number=nil
	local specials={
		[50]=KEY_SP_CR,
		[58]=KEY_SP_U,
		[59]=KEY_SP_D,
		[60]=KEY_SP_L,
		[61]=KEY_SP_R,
		[49]=KEY_SP_TAB,
		[51]=KEY_SP_BS,
		[52]=KEY_SP_DEL,
		[56]=KEY_SP_HOME,
		[57]=KEY_SP_END,
	}

	for i=0,3 do
		local iKey=peek(0xFF88+i)
		mods=(iKey==63) and (mods|MOD_CTRL) or mods
		mods=(iKey==64) and (mods|MOD_SHIFT) or mods
		mods=(iKey==65) and (mods|MOD_ALT) or mods

		if iKey>=1 and iKey<=26 then
			letter=iKey-1
		elseif iKey>=27 and iKey<=36 then
			number=iKey-27
		elseif iKey==37 then
			ascii="-"
		elseif iKey==38 then
			ascii="="
		elseif iKey==41 then
			ascii="\\"
		elseif iKey==45 then
			ascii=","
		elseif iKey==46 then
			ascii="."
		elseif iKey==47 then
			ascii="/"
		elseif iKey==48 then
			ascii=" "
		end
		
		if specials[iKey] then
			special=specials[iKey]
		end
	end

	if letter then
		ascii=string.char(
			letter
			+ (mods&MOD_SHIFT>0 and 65 or 97)
			)
	elseif number then
		local num_shift=")!\"#$%^&*("
		if mods&MOD_SHIFT>0 then
			ascii=num_shift:sub(number+1,number+1)
		else
			ascii=string.char(number+48)
		end
	end
	
	if (mods&MOD_SHIFT)>0 then
		if ascii=="-" then
			ascii="_"
		elseif ascii=="=" then
			ascii="+"
		elseif ascii=="," then
			ascii="<"
		elseif ascii=="." then
			ascii=">"
		end
	end
	
	return ascii,special,mods
end

function getBank(iBank)
	banks={
		{
			"-- Loaded from bank 1",
			"for y=0,136 do",
			"	for x=0,240 do",
			"		pix(x,y,(x+y>>3))",
			"	end",
			"end",
		},{
			"-- Loaded from bank 2",
			"for y=0,136 do",
			"	for x=0,240 do",
			"		pix(x,y,((120-x)^2+(68-y)^2)^.5)",
			"	end",
			"end",
		},{
			"-- Loaded from bank 3",
			"C=math.cos",
			"S=math.sin",
			"A=math.atan",
			"PI=math.pi",
			"for y=0,136 do",
			" for x=0,240 do",
			"  dx=120-x-C(T*.01)*100",
			"  dy=68-y-S(T*0.03)*50",
			"  d=(dx^2+dy^2)^.5",
			"  a=A(dx,dy)",
			"  p=d^0.8+S(3*a+T*0.04)^16*30",
			"  pix(x,y,p)",
			" end",
			"end",
		},
		[8]={
			"-- Just a little experiment",
			"--              / @jtruk :)",
			"--",
			"-- CTRL-1: Load from bank 1",
			"-- CTRL-2: Load from bank 2",
			"-- CTRL-3: Load from bank 3",
			"-- CTRL-8: Load instructions",
			"-- CTRL-9: Clear code",
			"-- CTRL-0: Run",
		},
		[9]={
			""
		}
	}
	return banks[iBank]
end
